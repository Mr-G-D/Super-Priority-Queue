package Helpers;

/**
 * @author DINESH KUMAR
 * Entry is a helper class to store key-value pair
 */

public class Entry{

    public int key;
    public int val;

    /**
     * Constructor to initialize Entry
     * @param k Key
     * @param v Value
     */
    public Entry(int k, int v){
        key = k;
        val = v;
    }
}
